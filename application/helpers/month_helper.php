<?php
	function get_month($name){
		if($name == "Januari"){
			return 1;
		}else if($name == "Februari"){
			return 2;
		}else if($name == "Maret"){
			return 3;
		}else if($name == "April"){
			return 4;
		}else if($name == "Mei"){
			return 5;
		}else if($name == "Juni"){
			return 6;
		}else if($name == "Juli"){
			return 7;
		}else if($name == "Agustus"){
			return 8;
		}else if($name == "September"){
			return 9;
		}else if($name == "Oktober"){
			return 10;
		}else if($name == "November"){
			return 11;
		}else if($name == "Desember"){
			return 12;
		}
	}